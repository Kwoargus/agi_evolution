def call_llm(prompt: str) -> str:
    """Вызывает Ollama и возвращает ответ."""
    response = ollama.chat(
        model='qwen2.5:7b',
        messages=[{'role': 'user', 'content': prompt}],
        options={'temperature': 0.3}
    )
    return response['message']['content'].strip()

def generate_analogies_for_invention(invention: Dict[str, Any]) -> List[Dict[str, Any]]:
    """
    Генерирует дополнительные аналоги и оценки для изобретения.
    """
    name = invention['name']
    task = invention.get('task', f"Устройство для {name}")
    properties = invention.get('properties', [])
    good = invention.get('good_analogy', [])
    bad = invention.get('bad_analogies', [])

    # Базовые примеры (известные)
    examples = []
    if good:
        examples.append({
            "task": task,
            "combination": good,
            "score": 0.95,
            "source": "manual_good"
        })
    for b in bad[:3]:
        examples.append({
            "task": task,
            "combination": b,
            "score": 0.2,  # условно
            "source": "manual_bad"
        })

    # Запрос к LLM для генерации новых комбинаций
    prompt = f"""
Ты — эксперт по истории изобретений и инженерии.

Дано изобретение: {name}
Задача: {task}
Ключевые свойства: {', '.join(properties) if properties else 'не указаны'}

Известные хорошие аналоги: {', '.join(good) if good else 'нет'}
Известные плохие аналоги: {', '.join([', '.join(b) for b in bad]) if bad else 'нет'}

Сгенерируй 5 новых возможных комбинаций (аналогов) для этого изобретения.
Для каждой комбинации укажи:
- комбинацию (список из 2-3 элементов),
- оценку соответствия задаче (от 0 до 1, где 1 – отлично подходит),
- краткое обоснование.

Ответ должен быть в формате JSON:
[
  {{"combination": ["элемент1", "элемент2"], "score": 0.85, "reason": "..."}},
  ...
]
"""
    response = call_llm(prompt)
    try:
        # Извлекаем JSON из ответа
        import re
        json_match = re.search(r'\[.*\]', response, re.DOTALL)
        if json_match:
            generated = json.loads(json_match.group())
            for item in generated:
                examples.append({
                    "task": task,
                    "combination": item.get("combination", []),
                    "score": item.get("score", 0.5),
                    "source": "llm_generated",
                    "reason": item.get("reason", "")
                })
        else:
            print(f"⚠️ Не удалось извлечь JSON для {name}: {response[:100]}")
    except json.JSONDecodeError as e:
        print(f"⚠️ Ошибка парсинга JSON для {name}: {e}")

    return examples

